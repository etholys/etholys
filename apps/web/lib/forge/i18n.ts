/** Map API invite error strings to forge i18n keys. */
export function mapForgeInviteError(message: string, locale: Locale = 'es'): string {
  const m = message.toLowerCase();
  if (m.includes('expirad') || m.includes('expired')) return forgeT('forge.invite.expired', locale);
  if (m.includes('inválid') || m.includes('invalid') || m.includes('no válid')) {
    return forgeT('forge.invite.invalid', locale);
  }
  if (m.includes('token')) return forgeT('forge.invite.incomplete', locale);
  return message;
}

/** Replace `{name}` placeholders in forge strings. */
export function forgeTFormat(
  key: string,
  locale: Locale,
  vars: Record<string, string | number>
): string {
  let s = forgeT(key, locale);
  for (const [k, v] of Object.entries(vars)) {
    s = s.replace(new RegExp(`\\{${k}\\}`, 'g'), String(v));
  }
  return s;
}

export function forgeT(key: string, locale: Locale = 'es'): string {
  return forge[locale]?.[key] ?? forge.es[key] ?? key;
}
